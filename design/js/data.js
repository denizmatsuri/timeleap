function generateResult({ era, country, name = '나' }) {
  const match = FEED.find(f => f.era === era && f.country === country);
  const eraObj = ERAS.find(e => e.id === era);
  const countryObj = COUNTRIES.find(c => c.code === country);
  const diary = ERA_DIARIES[era] || [
    `알 수 없는 시대의 어느 하루. 눈을 뜨니 모든 것이 낯설었다.`,
    `거리를 걸으며 내가 누구인지, 어디에 있는지 생각했다.`,
    `해가 지기 전 높은 곳에 올랐다. 이 시대의 지평선을 한 번은 봐 두고 싶었다.`,
  ];

  if (match) {
    return {
      ...match,
      id: 'result_' + Date.now(),
      author: name,
      date: eraObj.year + '.0' + (Math.floor(Math.random() * 9) + 1) + '.' + (10 + Math.floor(Math.random() * 18)),
      diary,
    };
  }

  return {
    id: 'result_' + Date.now(),
    author: name,
    era, country,
    city: countryObj.name,
    date: eraObj.year + '.0' + (Math.floor(Math.random() * 9) + 1) + '.15',
    title: eraObj.title + '의 어느 하루',
    excerpt: '시간을 거슬러 올라간 하루—',
    photos: [
      { scene: eraObj.title + ' ' + countryObj.name + '의 거리', palette: ['#2a1d3b','#c9944a','#f4b860'] },
      { scene: eraObj.title + ' 카페', palette: ['#3a2e20','#c9944a','#f1ead8'] },
      { scene: '노을 지는 풍경', palette: ['#d97f5a','#c9944a','#f4b860'] },
    ],
    tags: ['#' + eraObj.label, '#' + countryObj.name],
    likes: 0, views: 0,
    diary,
  };
}
